import {
  access,
  mkdir,
  readFile,
  readdir,
  rename,
  writeFile,
} from "node:fs/promises";
import { dirname, relative, resolve, sep } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const REVIEW_SLUG = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;
const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const repositoryRoot = resolve(scriptDirectory, "..");

export const defaultReviewsDirectory = resolve(
  repositoryRoot,
  "app/journey/reviews",
);
export const defaultOutputFile = resolve(
  repositoryRoot,
  "lib/generated/journey-review-registry.ts",
);
export const defaultTypesFile = resolve(
  repositoryRoot,
  "lib/journey-review-catalog-types.ts",
);

async function fileExists(path) {
  try {
    await access(path);
    return true;
  } catch (error) {
    if (error?.code === "ENOENT") return false;
    throw error;
  }
}

function moduleSpecifier(fromFile, toFile) {
  const path = relative(dirname(fromFile), toFile)
    .split(sep)
    .join("/");
  return path.startsWith(".") ? path : `./${path}`;
}

function identifierForSlug(slug) {
  return `Review_${slug.replaceAll("-", "_")}`;
}

export function assertJourneyReviewReleaseGate(slug, adapterModule) {
  if (adapterModule.journeyReviewReleaseReady !== true) {
    throw new Error(
      `Journey review "${slug}" is not release-ready and cannot be discovered.`,
    );
  }
}

export async function discoverJourneyReviewPackages(
  reviewsDirectory = defaultReviewsDirectory,
) {
  let entries;
  try {
    entries = await readdir(reviewsDirectory, { withFileTypes: true });
  } catch (error) {
    if (error?.code === "ENOENT") return [];
    throw error;
  }

  const packages = [];
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (!REVIEW_SLUG.test(entry.name)) {
      throw new Error(
        `Journey review route "${entry.name}" must use lowercase kebab-case.`,
      );
    }
    const reviewDirectory = resolve(reviewsDirectory, entry.name);
    const pageFile = resolve(reviewDirectory, "page.tsx");
    const providerFile = resolve(reviewDirectory, "provider.ts");
    const progressionAdapterFile = resolve(
      reviewDirectory,
      "progression-adapter.ts",
    );
    const hasPage = await fileExists(pageFile);
    const hasProvider = await fileExists(providerFile);
    const hasProgressionAdapter = await fileExists(progressionAdapterFile);
    if (!hasPage && !hasProvider && !hasProgressionAdapter) continue;
    if (!hasPage || !hasProvider || !hasProgressionAdapter) {
      throw new Error(
        `Journey review "${entry.name}" needs page.tsx, provider.ts, and progression-adapter.ts.`,
      );
    }
    let adapterModule;
    try {
      adapterModule = await import(
        pathToFileURL(progressionAdapterFile).href
      );
    } catch (error) {
      throw new Error(
        `Journey review "${entry.name}" release gate could not be loaded.`,
        { cause: error },
      );
    }
    assertJourneyReviewReleaseGate(entry.name, adapterModule);
    packages.push({
      slug: entry.name,
      providerFile,
      progressionAdapterFile,
    });
  }
  return packages.sort((left, right) => left.slug.localeCompare(right.slug));
}

export function renderJourneyReviewRegistry(
  packages,
  outputFile = defaultOutputFile,
  typesFile = defaultTypesFile,
) {
  const imports = packages
    .map(({ slug, providerFile }) => {
      const identifier = identifierForSlug(slug);
      const source = moduleSpecifier(outputFile, providerFile);
      return `import { reviewInfo as ${identifier}Info } from ${JSON.stringify(source)};`;
    })
    .join("\n");
  const entries = packages
    .map(({ slug }) => {
      const identifier = identifierForSlug(slug);
      return [
        "  {",
        `    ...${identifier}Info,`,
        `    slug: ${JSON.stringify(slug)},`,
        `    href: ${JSON.stringify(`/journey/reviews/${slug}/`)},`,
        '    role: "review",',
        "  },",
      ].join("\n");
    })
    .join("\n");
  const typesSource = moduleSpecifier(outputFile, typesFile);
  return [
    "/* This file is generated by scripts/generate-journey-review-registry.mjs. */",
    "/* Add review providers through app/journey/reviews/<slug>/provider.ts. */",
    "",
    `import type { JourneyReviewCatalogEntry } from ${JSON.stringify(typesSource)};`,
    imports,
    "",
    "export const discoveredJourneyReviews = [",
    entries,
    "] satisfies readonly JourneyReviewCatalogEntry[];",
    "",
  ]
    .filter((line, index, lines) => line !== "" || lines[index - 1] !== "")
    .join("\n");
}

export async function writeJourneyReviewRegistry({
  reviewsDirectory = defaultReviewsDirectory,
  outputFile = defaultOutputFile,
  typesFile = defaultTypesFile,
} = {}) {
  const packages = await discoverJourneyReviewPackages(reviewsDirectory);
  const content = renderJourneyReviewRegistry(packages, outputFile, typesFile);
  let existing = null;
  try {
    existing = await readFile(outputFile, "utf8");
  } catch (error) {
    if (error?.code !== "ENOENT") throw error;
  }
  if (existing === content) return { changed: false, packages };
  await mkdir(dirname(outputFile), { recursive: true });
  const temporaryFile = `${outputFile}.${process.pid}.${Date.now()}.tmp`;
  await writeFile(temporaryFile, content, "utf8");
  await rename(temporaryFile, outputFile);
  return { changed: true, packages };
}

const invokedPath = process.argv[1]
  ? pathToFileURL(resolve(process.argv[1])).href
  : null;

if (invokedPath === import.meta.url) {
  const { changed, packages } = await writeJourneyReviewRegistry();
  if (changed) {
    console.log(`Discovered ${packages.length} Journey review providers.`);
  }
}
