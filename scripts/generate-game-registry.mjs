import { access, mkdir, readFile, readdir, rename, writeFile } from "node:fs/promises";
import { dirname, relative, resolve, sep } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const GAME_SLUG = /^[a-z0-9]+(?:-[a-z0-9]+)*$/;
const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const repositoryRoot = resolve(scriptDirectory, "..");

export const defaultGamesDirectory = resolve(repositoryRoot, "app/games");
export const defaultOutputFile = resolve(
  repositoryRoot,
  "lib/generated/game-registry.ts",
);
export const defaultTypesFile = resolve(
  repositoryRoot,
  "lib/game-catalog-types.ts",
);

async function fileExists(path) {
  try {
    await access(path);
    return true;
  } catch (error) {
    if (error?.code === "ENOENT") return false;
    throw error;
  }
}

function moduleSpecifier(fromFile, toFile) {
  const withoutExtension = toFile.replace(/\.(?:tsx?|jsx?)$/, "");
  const path = relative(dirname(fromFile), withoutExtension).split(sep).join("/");
  return path.startsWith(".") ? path : `./${path}`;
}

function identifierForSlug(slug) {
  // Kebab-case to underscore is injective because underscores are not valid in
  // route slugs. The prefix also keeps digit-leading slugs valid TypeScript.
  return `Game_${slug.replaceAll("-", "_")}`;
}

function compareSlugs(left, right) {
  if (left < right) return -1;
  if (left > right) return 1;
  return 0;
}

export async function discoverGamePackages(
  gamesDirectory = defaultGamesDirectory,
) {
  const entries = await readdir(gamesDirectory, { withFileTypes: true });
  const packages = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;

    const gameDirectory = resolve(gamesDirectory, entry.name);
    const pageFile = resolve(gameDirectory, "page.tsx");
    if (!(await fileExists(pageFile))) continue;

    if (!GAME_SLUG.test(entry.name)) {
      throw new Error(
        `Game route "${entry.name}" must use lowercase kebab-case.`,
      );
    }

    const catalogFile = resolve(gameDirectory, "catalog.tsx");
    if (!(await fileExists(catalogFile))) {
      throw new Error(
        `Implemented game "${entry.name}" is missing catalog.tsx.`,
      );
    }

    packages.push({
      slug: entry.name,
      catalogFile,
    });
  }

  return packages.sort((left, right) => compareSlugs(left.slug, right.slug));
}

export function renderGameRegistry(
  packages,
  outputFile = defaultOutputFile,
  typesFile = defaultTypesFile,
) {
  const imports = packages
    .map(({ slug, catalogFile }) => {
      const identifier = identifierForSlug(slug);
      const source = moduleSpecifier(outputFile, catalogFile);
      return `import { ShelfIcon as ${identifier}ShelfIcon, gameInfo as ${identifier}GameInfo } from ${JSON.stringify(source)};`;
    })
    .join("\n");

  const entries = packages
    .map(({ slug }) => {
      const identifier = identifierForSlug(slug);
      return [
        "  {",
        `    ...${identifier}GameInfo,`,
        `    slug: ${JSON.stringify(slug)},`,
        `    href: ${JSON.stringify(`/games/${slug}/`)},`,
        `    ShelfIcon: ${identifier}ShelfIcon,`,
        "  },",
      ].join("\n");
    })
    .join("\n");

  const typesSource = moduleSpecifier(outputFile, typesFile);
  const importBlock = imports ? `${imports}\n` : "";

  return [
    "/* This file is generated by scripts/generate-game-registry.mjs. */",
    "/* Add games through app/games/<slug>/catalog.tsx, not here. */",
    "",
    `import type { GameCatalogEntry } from ${JSON.stringify(typesSource)};`,
    importBlock.trimEnd(),
    "",
    "export const discoveredGames = [",
    entries,
    "] satisfies readonly GameCatalogEntry[];",
    "",
  ]
    .filter((line, index, lines) => line !== "" || lines[index - 1] !== "")
    .join("\n");
}

export async function writeGameRegistry({
  gamesDirectory = defaultGamesDirectory,
  outputFile = defaultOutputFile,
  typesFile = defaultTypesFile,
} = {}) {
  const packages = await discoverGamePackages(gamesDirectory);
  const content = renderGameRegistry(packages, outputFile, typesFile);

  let existing = null;
  try {
    existing = await readFile(outputFile, "utf8");
  } catch (error) {
    if (error?.code !== "ENOENT") throw error;
  }

  if (existing === content) {
    return { changed: false, packages };
  }

  await mkdir(dirname(outputFile), { recursive: true });
  const temporaryFile = `${outputFile}.${process.pid}.${Date.now()}.tmp`;
  await writeFile(temporaryFile, content, "utf8");
  await rename(temporaryFile, outputFile);
  return { changed: true, packages };
}

const invokedPath = process.argv[1]
  ? pathToFileURL(resolve(process.argv[1])).href
  : null;

if (invokedPath === import.meta.url) {
  const { changed, packages } = await writeGameRegistry();
  if (changed) {
    console.log(`Discovered ${packages.length} implemented games.`);
  }
}
